#include <pic.h>			//包含头文件
/******************数据类型宏定义******************/
#define uchar unsigned char
#define uint unsigned int

/*******************************************
函数名称: Board_init
功    能: 初始化实验板
参    数: 无
返回值  : 无
/********************************************/
void Board_init(void)
{

  //*****将所有IO口全部配置为输出******************************************************
	TRISA=0x00;	   
	TRISB=0x00;
	TRISC=0x00;
	TRISD=0x00;
    TRISE=0X00;
  //************************************************************************

   
  //*****关闭LED1--LED8******************************************************
   PORTD=0XFF;   //PORTD输出高电平
   RA3=0;        //使能74HC573输出
   RC5=1;
   asm ("NOP");
   asm ("NOP");  
   RC5=0;         //74HC573锁存输出PORTD输出的高电平使LED关闭
  //************************************************************************

  //*****关闭数码管段选*****************************************************
   RC3=1;
   asm ("NOP");
   asm ("NOP"); 
   RC3=0;        //U2锁存高电平输出关闭数码管段选           
 //************************************************************************

 //*****关闭数码管位选*****************************************************
    RC4=1;
    PORTD=0X00;
    asm ("NOP");
    asm ("NOP"); 
    RC4=0;        //U3锁存高电平输出关闭数码管位选           
 //************************************************************************
    PORTD=0XFF;

    RC2=1;        //RC2输出高电位关闭LED10

    RE0=0;        //关闭蜂鸣器 
    RE1=0;        //关闭继电器
    RE2=0;        //设置MAX485为接收状态
}



